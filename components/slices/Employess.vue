<template>
  <v-container>
    <v-row justify="center" class="">
        <v-card v-for="(item, index) in slice.items" :key="index" class="ma-3" width="300">
          <v-card-title>{{ item.name }}</v-card-title>
          <v-card-subtitle>{{ item.role }}</v-card-subtitle>
          <v-card-text>
            <div class="mb-2">
              <a :href="`mailto:${item.email}`" class="grey--text">
                <v-icon class="mr-2">mdi-email</v-icon>{{ item.email }}
              </a>
            </div>
            <div>
              <a :href="`tel:+354 ${item.phone}`" class="grey--text">
                <v-icon color="husihus" size="20" class="mr-2">mdi-phone</v-icon>{{ formatPhoneNumber(item.phone) }}
              </a>
            </div>
          </v-card-text>
        </v-card>
      </v-row>

  </v-container>
</template>

<script>
export default {
  props: ['slice'],
  name: 'Employess',
  methods: {
    formatPhoneNumber(number) {
      const temp = number.replace(' ','').replace('-','');
      return temp.substring(0, 3) + " " + temp.substring(3);
    },
  }
}
</script>